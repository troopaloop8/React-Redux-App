export function getRandomInt(min, max) {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min)) + min;
}

export function randomDinoIndex() {
  return getRandomInt(0, dinosaurList.length - 1);
}

export function ranDino(arr, indexArr) {
  return arr[indexArr];
}
